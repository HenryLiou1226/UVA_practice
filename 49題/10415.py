dic = {
    "c": "0111001111",
    "d": "0111001110",
    "e": "0111001100",
    "f": "0111001000",
    "g": "0111000000",
    "a": "0110000000",
    "b": "0100000000",
    "C": "0010000000",
    "D": "1111001110",
    "E": "1111001100",
    "F": "1111001000",
    "G": "1111000000",
    "A": "1110000000",
    "B": "1100000000"
}
a = int(input())
for i in range(a):
    s = str(input())
    cur = ['0'] * 10
    ans = [0] * 10
    for j in s:
        for k in range(10):
            if dic[j][k] == '1' and cur[k] == '0':
                ans[k] += 1
            cur[k] = dic[j][k]
    for j in range(10):
        print(f'{ans[j]}', end=' ')
    print()